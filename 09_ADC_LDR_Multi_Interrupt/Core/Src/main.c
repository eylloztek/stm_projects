void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {

	if (hadc->Instance == ADC1) {

		static uint8_t conversionCounter = 0;

		adcValues[conversionCounter] = HAL_ADC_GetValue(hadc);

		if (conversionCounter == 0) {
			adcValuePot = adcValues[0];
		} else {
			adcValueLDR = adcValues[1];
			if (adcValueLDR < LIGHT_THRESHOLD) {
				HAL_GPIO_WritePin(led_GPIO_Port, led_Pin, 1);    // LED ON
				ledStatus = 1;
			} else {
				HAL_GPIO_WritePin(led_GPIO_Port, led_Pin, 0); // LED OFF
				ledStatus = 0;
			}

		}
		conversionCounter++;

		if (conversionCounter >= 2) {
			conversionCounter = 0;
		}

	}

}

